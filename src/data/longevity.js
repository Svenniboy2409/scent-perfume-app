// Resolves the longevity to display for a perfume: explicit rating if present,
// otherwise a concentration-based default (finally 3 as a last resort).
export function getLongevity(perfume) {
  const rating =
    LONGEVITY[perfume.id] ?? CONCENTRATION_DEFAULT[perfume.concentration] ?? 3
  return { rating, ...LONGEVITY_LEVELS[rating] }
}
